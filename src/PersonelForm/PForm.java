    private void text_araKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_araKeyReleased
        //PARAMETRE SEÇİMİNE GÖRE PERSONEL ARAMA İŞLEMİNİ GERÇEKLEŞTİREN KOMUTLAR
        
        if(combo_ara.getSelectedIndex() <=0 ){ //HİÇBİR PARAMETRE SEÇİLİ DEĞİL İSE ;
            JOptionPane.showMessageDialog(rootPane, "Lütfen Geçerli Bir Parametre Seçiniz");
        }
        else{
            Connection con;
            String PID,PAD,PSOYAD,PTC,PCINSIYET,PEHLIYET,PEV,PCEP,PEPOSTA,PSEHIR,PMAAS,PIZIN,PDEPARTMAN,PPOZISYON;
           
            try{
                 con = Connectionz.getConnection();
                 String str1=text_ara.getText();
                 String sql = "Select * from tbl_personel where "+option+"=?";
                 PreparedStatement pst = con.prepareStatement(sql);
                 pst.setString(1,str1);
                 ResultSet rs = pst.executeQuery();
                 dtm =(DefaultTableModel)tbl_listele.getModel();
                 dtm.setRowCount(0);
                 if(rs.next()){
                     do{
                         PID = rs.getString("PID");
                         PAD = rs.getString("PAD");
                         PSOYAD = rs.getString("PSOYAD");
                         PTC = rs.getString("PTCKNO");
                         PCINSIYET = rs.getString("PCINSIYET");
                         PEHLIYET = rs.getString("PEHLIYET");
                         PEV = rs.getString("PEVTEL");
                         PCEP = rs.getString("PCEPTEL");
                         PEPOSTA = rs.getString("PEPOSTA");
                         PSEHIR = rs.getString("PSEHIR");
                         PMAAS = rs.getString("PMAAS");
                         PIZIN = rs.getString("PIZINGUNU");
                         PDEPARTMAN = rs.getString("PDEPARTMAN");
                         PPOZISYON = rs.getString("PPOZISYON");
                         dtm.addRow(new Object[]{PID,PAD,PSOYAD,PTC,PCINSIYET,PEHLIYET,PEV,PCEP,PEPOSTA,PSEHIR,PMAAS,PIZIN,PDEPARTMAN,PPOZISYON});
                     }while(rs.next());
                 }
            }
            
            catch(Exception ex){
                System.out.println(""+ex);
            }
        }
    }//GEN-LAST:event_text_araKeyReleased

